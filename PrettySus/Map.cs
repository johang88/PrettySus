        public static bool PlayerCollides(float x, float y)
        {
            var leftTile = (int)(x /TileSize);
            var topTile = (int)Math.Ceiling((y + Constants.PlayerHeight / 2.0f) / Map.TileSize) - 1; // this allows player to overlap slightly
            var rightTile = (int)Math.Ceiling((x + Constants.PlayerWidth) / Map.TileSize) - 1;
            var bottomTile = (int)Math.Ceiling((y + Constants.PlayerHeight) / Map.TileSize) - 1;

            for (int ty = topTile; ty <= bottomTile; ++ty)
            {
                for (int tx = leftTile; tx <= rightTile; ++tx)
                {
                    if (ty >= 0 && tx >= 0 && ty < Height && tx < Width)
                    {
                        var index = ty * Width + tx;
                        if (Data[index] == 1)
                        {
                            return true;
                        }
                    }
                }
            }

            return false;
        }
